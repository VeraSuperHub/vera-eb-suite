# Compute ranks
def rank_array(arr):
    """Rank from 1 (highest) to n (lowest)."""
    temp = np.argsort(-arr)
    ranks = np.empty_like(temp, dtype=float)
    ranks[temp] = np.arange(1, len(arr) + 1)
    return ranks

ranks_a_mnl = rank_array(imp_a_mnl)
