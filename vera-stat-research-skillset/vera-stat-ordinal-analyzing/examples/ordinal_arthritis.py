# --- 6B-2: Brant Test (Manual Implementation) ---
print("── 6B-2: Brant Test for Proportional Odds Assumption ──")
print("  (Manual Wald-type test: comparing binary logistic coefficients across thresholds)\n")

# Fit separate binary logistic regressions at each threshold
brant_coefs = []
brant_ses = []
thresholds = [(0, [1, 2]), (1, [2])]  # None vs (Some+Marked), (None+Some) vs Marked
threshold_labels = ["None vs Some+Marked", "None+Some vs Marked"]

for t_idx, (cut, higher) in enumerate(thresholds):
    y_bin = (arth["Improved_num"] > cut).astype(int)
    X_bin = sm.add_constant(arth[predictor_cols].astype(float))
    try:
        bin_model = sm.Logit(y_bin, X_bin).fit(disp=0)
        brant_coefs.append(bin_model.params.values[1:])  # skip intercept
        brant_ses.append(bin_model.bse.values[1:])
    except Exception as e:
        print(f"  Warning: Binary logit at threshold {t_idx} failed: {e}")
        brant_coefs.append(np.full(n_pred, np.nan))
        brant_ses.append(np.full(n_pred, np.nan))

if len(brant_coefs) == 2 and not np.any(np.isnan(brant_coefs[0])):
    coef_diff = brant_coefs[0] - brant_coefs[1]
    se_diff = np.sqrt(brant_ses[0]**2 + brant_ses[1]**2)
    wald_stats = (coef_diff / se_diff) ** 2  # chi-sq with df=1
    brant_p = [1 - stats.chi2.cdf(w, df=1) for w in wald_stats]

    # Overall Brant
    overall_chi2 = np.sum(wald_stats)
    overall_df = n_pred
    overall_p = 1 - stats.chi2.cdf(overall_chi2, df=overall_df)

    print(f"  Overall: chi2({overall_df}) = {overall_chi2:.2f}, p = {fmt_p(overall_p)}")
    print(f"  Per-predictor:")
    for j, pname in enumerate(predictor_labels):
        print(f"    {pname:25s}: chi2(1) = {wald_stats[j]:.2f}, p = {fmt_p(brant_p[j])}")

    if overall_p >= 0.05:
        brant_met = True
        print("\n  DECISION: The proportional odds assumption was met (p >= .05).")
        print("  The cumulative logit model is appropriate as the primary ordinal model.")
    else:
        brant_met = False
        violated = [predictor_labels[j] for j in range(n_pred) if brant_p[j] < 0.05]
        print(f"\n  DECISION: The proportional odds assumption was violated (p = {fmt_p(overall_p)}).")
        if violated:
            print(f"  Violating predictors: {', '.join(violated)}")
        print("  Adjacent-category, continuation-ratio, and stereotype models provide alternatives.")
else:
    brant_met = True
    print("  Could not compute Brant test. Proceeding with proportional odds as primary.")
print()

